package com.softtest.softtest.entity;

public class QuestionInfoExample {
    private Integer questionId;
    private Integer typeId;
    private Integer knowledgePoint;
    private Integer projectId;
    private Integer authorId;
    private Integer languageId;

    public QuestionInfoExample() {
    }

    public Integer getQuestionId() {
        return questionId;
    }

    public void setQuestionId(Integer questionId) {
        this.questionId = questionId;
    }

    public Integer getTypeId() {
        return typeId;
    }

    public void setTypeId(Integer typeId) {
        this.typeId = typeId;
    }

    public Integer getKnowledgePoint() {
        return knowledgePoint;
    }

    public void setKnowledgePoint(Integer knowledgePoint) {
        this.knowledgePoint = knowledgePoint;
    }

    public Integer getProjectId() {
        return projectId;
    }

    public void setProjectId(Integer projectId) {
        this.projectId = projectId;
    }

    public Integer getAuthorId() {
        return authorId;
    }

    public void setAuthorId(Integer authorId) {
        this.authorId = authorId;
    }

    public Integer getLanguageId() {
        return languageId;
    }

    public void setLanguageId(Integer languageId) {
        this.languageId = languageId;
    }
}
